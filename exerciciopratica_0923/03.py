# Defina o número de alunos e suas respectivas idades diretamente no código
num_alunos = 5  # Exemplo: 5 alunos
idades = [18, 22, 20, 21, 19]  # Exemplo de idades dos alunos

# Calcula a soma das idades
soma_idades = sum(idades)

# Calcula a média aritmética
media_idades = soma_idades / num_alunos

# Exibe o resultado
print(f"A idade média dos alunos é: {media_idades}")



#Explicação:
#O número de alunos é predefinido com o valor 5 e as idades dos alunos são fornecidas diretamente na lista idades.
#O programa calcula a soma das idades e, em seguida, divide pelo número de alunos para obter a média.
#Você pode ajustar a quantidade de alunos e as idades de acordo com suas necessidades.